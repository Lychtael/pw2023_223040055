<?php
// 1. inisialisasi / nilai awal
// 2. kondisi terminasi / kapan pengulangan berhenti
// 3. increment / decrement
$nomer = 1; // inisialisasi
$angkot = 10;

while ($nomer <= $angkot) { // kondisi terminasi
    echo "Angkot no.$nomer beroperasi dengan baik. <br>";
    $nomer++; // increment
}
